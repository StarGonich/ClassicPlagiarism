x = int(input())  # мода
y = int(input())  # медиана
z = int(input())  # среднее арифметическое

# Нам нужно найти 5 чисел: a, b, c, d, e, где:
# 1. c = y (медиана — центральное число в отсортированном списке)
# 2. одно из чисел должно быть x (мода, причём единственная)
# 3. (a + b + c + d + e) / 5 = z (среднее арифметическое)

# Пусть числа в порядке возрастания: a ≤ b ≤ c ≤ d ≤ e
# c = y (по определению медианы)
c = y

# Так как мода должна быть равна x и быть единственной,
# x должно встречаться чаще других чисел, но не более 2 раз (иначе не будет единственной моды).
# Значит, x может быть a и b или d и e.

# Попробуем вариант, когда x встречается в начале: a = x, b = x
a = x
b = x

# Тогда условие на среднее: (x + x + y + d + e) / 5 = z
# Умножаем на 5: 2x + y + d + e = 5z
# Отсюда: d + e = 5z - 2x - y
sum_de = 5 * z - 2 * x - y

# Выберем d и e так, чтобы они были больше y (так как c = y — медиана)
# и чтобы x не было модой для d и e (иначе мода не будет единственной).
# Например, возьмём d = y + 1, тогда e = sum_de - d
d = y + 1
e = sum_de - d

# Проверим, что все числа в диапазоне [-10^3, 10^3]
if -10**3 <= a <= 10**3 and -10**3 <= b <= 10**3 and -10**3 <= c <= 10**3 and -10**3 <= d <= 10**3 and -10**3 <= e <= 10**3:
    print(a, b, c, d, e)
else:
    print(-1)  # Если числа выходят за диапазон, выводим -1 (по условию — если решений нет, выводим 1, адаптируем)
