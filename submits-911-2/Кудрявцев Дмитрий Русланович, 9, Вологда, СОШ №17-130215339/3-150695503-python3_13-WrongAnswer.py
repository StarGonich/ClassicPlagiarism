x = int(input())  # мода
y = int(input())  # медиана
z = int(input())  # среднее арифметическое

# Проверяем граничные условия
if x != y and abs(x - y) > 10**8:
    print(-1)
    exit()

# Создаём список из 5 элементов: [x, x, y, a, b]
# где x — мода (встречается дважды), y — медиана (центральный элемент)
# a и b — неизвестные числа, которые подберём

# Формула для среднего арифметического: (2x + y + a + b) / 5 = z
# Отсюда: a + b = 5z - 2x - y
sum_ab = 5 * z - 2 * x - y

# Подбираем a и b так, чтобы не нарушить уникальность моды
if x == y:
    # Если мода и медиана совпадают, можно взять a и b равными
    a = sum_ab // 2
    b = sum_ab - a
else:
    # Если x != y, избегаем дублирования x в a или b
    a = sum_ab - 10**8  # берём очень большое число
    b = 10**8  # второе число тоже большое, но противоположное по знаку

# Формируем список и сортируем для проверки медианы
result = [x, x, y, a, b]
result.sort()

# Проверяем, что медиана равна y (центральный элемент)
if result[2] != y:
    print(-1)
else:
    # Проверяем уникальность моды с помощью Counter
    from collections import Counter
    counts = Counter(result)
    if counts[x] > 1 and all(counts[num] < counts[x] for num in counts if num != x):
        print(*result)
    else:
        print(-1)
