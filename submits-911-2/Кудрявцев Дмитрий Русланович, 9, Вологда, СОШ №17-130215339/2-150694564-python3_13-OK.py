# Считываем вероятности с стандартного ввода
p1 = float(input())
p2 = float(input())
p3 = float(input())

# Вычисляем вероятность верного диагноза по большинству голосов
# Случай 1: все три теста верны
prob_all_correct = p1 * p2 * p3

# Случай 2: первый и второй тесты верны, третий — нет
prob_1_2_correct = p1 * p2 * (1 - p3)

# Случай 3: первый и третий тесты верны, второй — нет
prob_1_3_correct = p1 * (1 - p2) * p3

# Случай 4: второй и третий тесты верны, первый — нет
prob_2_3_correct = (1 - p1) * p2 * p3

# Суммируем вероятности всех благоприятных случаев
total_probability = prob_all_correct + prob_1_2_correct + prob_1_3_correct + prob_2_3_correct

# Выводим результат с точностью до 10^-4
print(f"{total_probability:.4f}")
