import sys

def main():
    input = sys.stdin.read
    data = input().split()
    N = int(data[0])
    T = int(data[1])
    intervals = data[2:]
    
    # Максимальный возможный момент времени (по условию)
    MAX_TIME = 10**9
    
    # Используем массив разностей для быстрого подсчета
    # Размер массива должен покрывать все возможные времена
    # Но из-за очень большого диапазона (до 10^9), прямое создание массива невозможно.
    # Поэтому будем использовать скользящее окно и события.
    
    # Для оптимизации используем "события" (line sweep)
    events = []
    for i in range(N):
        t1 = int(intervals[2*i])
        t2 = int(intervals[2*i + 1])
        events.append((t1, 1))
        events.append((t2, -1))
    
    # Сортируем события по времени
    events.sort()
    
    # Проходим по времени и считаем сумму рекламы в любой момент времени
    current_sum = 0
    time_points = []
    last_time = 0
    
    # Создаем список (time, cumulative_reklama) для всех точек времени
    # В связи с возможными большими временными промежутками, используем скользящий подсчет
    # и проверку интервалов на лету
    
    # Идея:
    # - Проходим по событиям
    # - На каждом событии обновляем текущий счетчик рекламы
    # - Между событиями реклама остается постоянной
    # - Проверяем все интервалов длиной 3600 секунд, начиная с текущего времени
    
    # Для этого:
    # - Идем по событиям, и на каждом этапе:
    #   * Обновляем текущий уровень рекламы
    #   * Проверяем, есть ли интервал [start, start + 3600), где сумма превышает T
    
    # Однако, поскольку события могут быть разбросаны очень широко (до 10^9),
    # лучше использовать двоичный поиск по времени, чтобы находить все возможные интерваллы.
    #
    # Для оптимизации:
    # - Собираем все точки времени, где изменяется сумма рекламы.
    # - Для каждого такого момента проверяем интервал длиной 3600 секунд, начиная с этого времени.
    
    # Собираем все ключевые точки времени
    key_times = []
    current_reklama = 0
    for i in range(len(events)):
        time, delta = events[i]
        key_times.append(time)
        current_reklama += delta
    
    # Также добавим границы
    # Важная часть: чтобы проверить все возможные интерваллы, нужно рассматривать все точки, где меняется сумма,
    # а также возможные стартовые точки интервала.
    #
    # Для этого можно пройтись по отсортированным ключевым точкам времени и для каждого проверить интервал [time, time+3600)
    # через двоичный поиск, чтобы найти сумму рекламы в этом интервале.
    
    import bisect
    
    # Создадим массив префиксных сумм
    # Но так как у нас только точки, то нужно построить массив, где для каждого времени хранится сумма рекламы
    # до этого времени.
    
    # Воспользуемся списком times и значениями рекламы в этих точках
    # и построим префиксные суммы
    
    times = []
    ads = []
    total_reklama = 0
    for time, delta in events:
        total_reklama += delta
        times.append(time)
        ads.append(total_reklama)
    
    # Теперь для каждого возможного начала интервала ищем сумму рекламы
    # с помощью двоичного поиска
    
    min_start = -1
    for i in range(len(times)):
        start_time = times[i]
        end_time = start_time + 3600
        # Находим индекс первого времени >= start_time
        left_idx = i
        # Находим индекс первого времени >= end_time
        right_idx = bisect.bisect_left(times, end_time)
        
        sum_in_interval = ads[right_idx - 1] - (ads[left_idx - 1] if left_idx > 0 else 0)
        if sum_in_interval > T:
            min_start = start_time
            break
    
    print(min_start)
    

if __name__ == "__main__":
    main()