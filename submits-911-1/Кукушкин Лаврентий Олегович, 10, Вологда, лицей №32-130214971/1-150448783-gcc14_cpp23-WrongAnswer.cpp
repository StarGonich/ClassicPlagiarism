#include <iostream>

using namespace std;

int main()
{
    long long h1,m1,h2,m2,k=0;
    cin >> h1 >> m1 >> h2 >> m2;
    if(h1==h2)
    {
        for(int e=m1; e<=m2; e++)
        {
            if ((h1==e)||((h1==((e%10)*10+e/10))&&((h1%10)!=6)&&((h1%10)!=7)&&((h1%10)!=8)&&((h1%10)!=9))||(h1==((e/10-2)*10+e%10-2)))
            {
                k++;
            }
        }
    }
    else
    {
        for(int g=m1; g<60; g++)
        {
            if ((h1==g)||((h1==((g%10)*10+g/10))&&((h1%10)!=6)&&((h1%10)!=7)&&((h1%10)!=8)&&((h1%10)!=9))||(h1==((g/10-2)*10+g%10-2)))
            {
                k++;
            }
        }
        for (int i=h1+1; i<h2; i++)
        {
            for(int p=0; p<60; p++)
            {
                if ((i==p)||((i==((p%10)*10+p/10))&&((i%10)!=6)&&((i%10)!=7)&&((i%10)!=8)&&((i%10)!=9))||(i==((p/10-2)*10+p%10-2)))
                {
                    k++;
                }
            }
        }
        for(int y=0; y<=m2; y++)
        {
            if ((h2==y)||((h2==((y%10)*10+y/10))&&((h2%10)!=6)&&((h2%10)!=7)&&((h2%10)!=8)&&((h2%10)!=9))||(h2==((y/10-2)*10+y%10-2)))
            {
                k++;
            }
        }
    }
    cout << k << endl;
    return 0;
}
